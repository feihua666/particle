-- 建表语句sql
DROP TABLE IF EXISTS component_tenant_create_apply;
CREATE TABLE `component_tenant_create_apply` (
  `id` bigint NOT NULL COMMENT '租户ID',
  `name` varchar(100) NOT NULL COMMENT '租户名称,模糊查询',
  `contact_user_name` varchar(200) DEFAULT NULL COMMENT '联系人姓名，没有指定 applyUserId 时，用户创建用户昵称和姓名',
  `contact_user_email` varchar(300) DEFAULT NULL COMMENT '联系人邮箱，没有指定 applyUserId 时，用户创建用户登录账号',
  `contact_user_phone` varchar(255) DEFAULT NULL COMMENT '联系人电话，没有指定 applyUserId 时，用户创建用户登录账号',
  `tenant_type_dict_id` bigint NOT NULL COMMENT '租户类型字典id',
  `apply_user_id` bigint DEFAULT NULL COMMENT '申请用户，如果主空意味着联系邮箱和联系电话必须填写一个，将用户创建关联用户',
  `audit_status_dict_id` bigint NOT NULL COMMENT '审核状态，字典id',
  `audit_status_comment` varchar(255) DEFAULT NULL COMMENT '审核意见',
  `audit_user_id` bigint DEFAULT NULL COMMENT '审核用户id',
  `applied_tenant_id` bigint DEFAULT NULL COMMENT '审核通过后创建的租户id',
  `is_formal` tinyint(1) NOT NULL COMMENT '是否正式，1=正式，0=试用',
  `user_limit_count` int NULL DEFAULT NULL COMMENT '用户数量限制',
  `effective_at` datetime NULL DEFAULT NULL COMMENT '生效日期，从什么时候开始生效',
  `effective_days` int NULL DEFAULT NULL COMMENT '申请天数',
  `expire_at` datetime NULL DEFAULT NULL COMMENT '过期时间，从什么时候失效',
  `ext_json` mediumtext DEFAULT NULL COMMENT '额外申请项json，如：应用和功能',
  `remark` varchar(255) DEFAULT NULL COMMENT '描述',
  `version` int NOT NULL COMMENT '乐观锁字段',
  `tenant_id` bigint DEFAULT NULL COMMENT '租户id',
  `create_at` datetime NOT NULL COMMENT '创建时间的时间戳',
  `create_by` bigint DEFAULT NULL COMMENT '创建人',
  `update_at` datetime DEFAULT NULL COMMENT '修改时间的时间戳',
  `update_by` bigint DEFAULT NULL COMMENT '修改人',
  PRIMARY KEY (`id`) USING BTREE,
  KEY `name` (`name`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='租户创建申请表';
