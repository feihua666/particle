-- 建表语句sql
DROP TABLE IF EXISTS component_data_company_person;
CREATE TABLE `component_data_company_person` (
  `id` bigint NOT NULL COMMENT '主键ID',
  `name` varchar(50) NOT NULL COMMENT '姓名',
  `id_no` varchar(18) DEFAULT NULL COMMENT '证号',
  `id_no_desensitized` varchar(18) DEFAULT NULL COMMENT '脱敏证号',
  `id_no_md5` varchar(32) DEFAULT NULL COMMENT '证号md5',
  `id_no_sha256` varchar(64) DEFAULT NULL COMMENT '证号sha256',
  `id_no_sm3` varchar(64) DEFAULT NULL COMMENT '证号sm3',
  `id_no_encrypt` varchar(500) DEFAULT NULL COMMENT '证号密文',
  `mobile` varchar(11) DEFAULT NULL COMMENT '手机号',
  `mobile_operator_dict_id` bigint DEFAULT NULL COMMENT '手机号运营商',
  `mobile1` varchar(11) DEFAULT NULL COMMENT '手机号1',
  `mobile1_operator_dict_id` bigint DEFAULT NULL COMMENT '手机号1运营商',
  `mobile2` varchar(11) DEFAULT NULL COMMENT '手机号2',
  `mobile2_operator_dict_id` bigint DEFAULT NULL COMMENT '手机号2运营商',
  `handle_remark` varchar(500) DEFAULT NULL COMMENT '处理备注，用来标识一些处理备注',
  `latest_handle_at` datetime DEFAULT NULL COMMENT '最后处理时间，不代表数据有变动，用来表示数据处理过，但可能无需处理，不影响版本号变动',
  `version` int NOT NULL COMMENT '乐观锁字段',
  `tenant_id` bigint DEFAULT NULL COMMENT '租户id',
  `create_at` datetime NOT NULL COMMENT '创建时间的时间戳',
  `create_by` bigint DEFAULT NULL COMMENT '创建人',
  `update_at` datetime DEFAULT NULL COMMENT '修改时间的时间戳',
  `update_by` bigint DEFAULT NULL COMMENT '修改人',
  PRIMARY KEY (`id`) USING BTREE,
  KEY `name` (`name`) USING BTREE,
  UNIQUE KEY `id_no` (`id_no`) USING BTREE,
  KEY `id_no_desensitized` (`id_no_desensitized`) USING BTREE,
  UNIQUE KEY `id_no_md5` (`id_no_md5`) USING BTREE,
  UNIQUE KEY `id_no_sha256` (`id_no_sha256`) USING BTREE,
  UNIQUE KEY `id_no_sm3` (`id_no_sm3`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='企业个人表';
