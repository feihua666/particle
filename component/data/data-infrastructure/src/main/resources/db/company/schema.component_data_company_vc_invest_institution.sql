-- 建表语句sql
DROP TABLE IF EXISTS component_data_company_vc_invest_institution;
CREATE TABLE `component_data_company_vc_invest_institution` (
  `id` bigint NOT NULL COMMENT '主键ID',
  `company_id` bigint DEFAULT NULL COMMENT '企业表ID，投资机构可能不对应公司',
  `is_related_company` tinyint(1) DEFAULT NULL COMMENT '是否对应公司，如果company_id为null不确定是数据没处理还是没对应公司，这里加一个字段标识',
  `name` varchar(100) DEFAULT NULL COMMENT '机构名称',
  `en_name` varchar(100) DEFAULT NULL COMMENT '机构英文名称',
  `logo_url` varchar(300) DEFAULT NULL COMMENT '机构logo地址',
  `website_url` varchar(300) DEFAULT NULL COMMENT '机构网址',
  `establish_year` int DEFAULT NULL COMMENT '成立年份，可以成立日期没有值只有年份',
  `establish_date` date DEFAULT NULL COMMENT '成立日期',
  `province_area_id` bigint DEFAULT NULL COMMENT '所在省份，区域id',
  `city_area_id` bigint DEFAULT NULL COMMENT '所在城市，区域id',
  `county_area_id` bigint DEFAULT NULL COMMENT '所在区县，区域id',
  `address` varchar(200) DEFAULT NULL COMMENT '机构地址',
  `mobile` varchar(20) DEFAULT NULL COMMENT '手机号码',
  `telephone` varchar(20) DEFAULT NULL COMMENT '电话号码',
  `email` varchar(200) DEFAULT NULL COMMENT '邮箱',
  `weibo_url` varchar(300) DEFAULT NULL COMMENT '微博地址',
  `wechat_no` varchar(50) DEFAULT NULL COMMENT '微信号',
  `description` text COMMENT '机构介绍',
  `data_md5` varchar(32) DEFAULT NULL COMMENT '数据md5,name + en_name',
  `latest_handle_at` datetime DEFAULT NULL COMMENT '最后处理时间，不代表数据有变动，用来表示数据处理过，但可能无需处理，不影响版本号变动',
  `version` int NOT NULL COMMENT '乐观锁字段',
  `tenant_id` bigint DEFAULT NULL COMMENT '租户id',
  `create_at` datetime NOT NULL COMMENT '创建时间的时间戳',
  `create_by` bigint DEFAULT NULL COMMENT '创建人',
  `update_at` datetime DEFAULT NULL COMMENT '修改时间的时间戳',
  `update_by` bigint DEFAULT NULL COMMENT '修改人',
  PRIMARY KEY (`id`) USING BTREE,
  KEY `company_id` (`company_id`) USING BTREE,
  KEY `name` (`name`) USING BTREE,
  KEY `en_name` (`en_name`) USING BTREE,
  UNIQUE KEY `uni_data_md5` (`data_md5`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='企业投资机构表';
