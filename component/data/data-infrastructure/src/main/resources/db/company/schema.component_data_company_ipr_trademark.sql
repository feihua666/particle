DROP TABLE IF EXISTS component_data_company_ipr_trademark;
CREATE TABLE `component_data_company_ipr_trademark` (
  `id` bigint NOT NULL COMMENT '主键ID',
  `name` varchar(200) DEFAULT NULL COMMENT '商标名称',
  `name_cn` varchar(200) DEFAULT NULL COMMENT '商标名称',
  `name_en` varchar(200) DEFAULT NULL COMMENT '商标名称',
  `reg_no` varchar(50) NOT NULL COMMENT '注册号',
  `apply_no` varchar(50) DEFAULT NULL COMMENT '申请号',
  `nice_category_no` varchar(50) DEFAULT NULL COMMENT '尼斯分类号',
  `right_status_name` varchar(50) DEFAULT NULL COMMENT '当前权利状态名称，如：已注册、待审中',
  `type_name` varchar(50) DEFAULT NULL COMMENT '商标类型名称，如：普通商标',
  `trademark_image_url` varchar(300) DEFAULT NULL COMMENT '商标图片地址',
  `reg_date` date DEFAULT NULL COMMENT '注册日期',
  `apply_date` date DEFAULT NULL COMMENT '申请日期',
  `is_share` tinyint(1) DEFAULT NULL COMMENT '是否共享，1=共享，0=不共享',
  `is_well_known` tinyint(1) DEFAULT NULL COMMENT '是否驰名商标，1=驰名，0=非驰名',
  `is_specify_color` tinyint(1) DEFAULT NULL COMMENT '是否指定颜色，1=是，0=否',
  `priority_date` date DEFAULT NULL COMMENT '优先权日期',
  `special_start_date` date DEFAULT NULL COMMENT '专用权期限开始日期',
  `special_end_date` date DEFAULT NULL COMMENT '专用权期限截止日期',
  `first_trial_public_date` date DEFAULT NULL COMMENT '初审公告日期',
  `first_trial_public_no` varchar(50) DEFAULT NULL COMMENT '初审公告号',
  `late_specify_date` date DEFAULT NULL COMMENT '后期指定日期',
  `dissent_end_date` date DEFAULT NULL COMMENT '异议截止日期',
  `origin_lang` varchar(200) DEFAULT NULL COMMENT '原始语种',
  `reg_public_issue_no` varchar(50) DEFAULT NULL COMMENT '注册公告期号',
  `international_reg_date` date DEFAULT NULL COMMENT '国际注册日期',
  `latest_handle_at` datetime DEFAULT NULL COMMENT '最后处理时间，不代表数据有变动，用来表示数据处理过，但可能无需处理，不影响版本号变动',
  `version` int NOT NULL COMMENT '乐观锁字段',
  `tenant_id` bigint DEFAULT NULL COMMENT '租户id',
  `create_at` datetime NOT NULL COMMENT '创建时间的时间戳',
  `create_by` bigint DEFAULT NULL COMMENT '创建人',
  `update_at` datetime DEFAULT NULL COMMENT '修改时间的时间戳',
  `update_by` bigint DEFAULT NULL COMMENT '修改人',
  PRIMARY KEY (`id`) USING BTREE,
  UNIQUE KEY `reg_no` (`reg_no`) USING BTREE,
  UNIQUE KEY `apply_no` (`apply_no`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='企业知识产权商标表';
