-- 建表语句sql
DROP TABLE IF EXISTS component_data_company_administrative_license;
CREATE TABLE `component_data_company_administrative_license` (
  `id` bigint NOT NULL COMMENT '主键ID',
  `company_id` bigint NOT NULL COMMENT '企业表ID',
  `licence_no` varchar(255) DEFAULT NULL COMMENT '许可编号 ,许可文件编号，（信用中国、工商公示）',
  `licence_name` varchar(255) DEFAULT NULL COMMENT '许可证书名称,许可文件名称，（信用中国、工商公示）',
  `decision_document_no` varchar(255) DEFAULT NULL COMMENT '行政许可决定文书号，（信用中国、工商公示）',
  `decision_document_name` varchar(255) DEFAULT NULL COMMENT '许可证决定文书名称，（信用中国、工商公示）',
  `decision_date` date DEFAULT NULL COMMENT '许可决定日期，（信用中国、工商公示）',
  `from_date` date DEFAULT NULL COMMENT '许可开始日期，（信用中国、工商公示）',
  `end_date` date DEFAULT NULL COMMENT '许可截止日期，（信用中国、工商公示）',
  `licence_content` text COMMENT '许可内容，（信用中国、工商公示）',
  `institute` varchar(100) DEFAULT NULL COMMENT '许可机关，（信用中国、工商公示）',
  `institute_uscc` varchar(100) DEFAULT NULL COMMENT '许可机关统一社会信用代码 ，（信用中国）',
  `institute_company_id` bigint DEFAULT NULL COMMENT '许可机关公司id',
  `data_from` varchar(100) DEFAULT NULL COMMENT '数据来源单位 ，（信用中国）',
  `data_from_uscc` varchar(100) DEFAULT NULL COMMENT '数据来源单位统一社会信用代码，（信用中国）',
  `data_from_company_id` bigint DEFAULT NULL COMMENT '数据来源单位公司id',
  `licence_type` varchar(100) DEFAULT NULL COMMENT '许可类型，审核类型，（工商公示、信用中国）',
  `licence_remark` varchar(200) DEFAULT NULL COMMENT '许可备注，（工商公示）',
  `is_data_flag_gs` tinyint(1) DEFAULT NULL COMMENT '是否数据标识为工商公示，1=工商公示，0=非工商公示',
  `is_data_flag_xyzg` tinyint(1) DEFAULT NULL COMMENT '是否数据标识为信用中国，1=信用中国，0=非信用中国',
  `data_md5` varchar(32) DEFAULT NULL COMMENT '数据md5,decision_document_no + decision_date + licence_content + licence_type + is_data_flag_gs + is_data_flag_xyzg',
  `latest_handle_at` datetime DEFAULT NULL COMMENT '最后处理时间，不代表数据有变动，用来表示数据处理过，但可能无需处理，不影响版本号变动',
  `version` int NOT NULL COMMENT '乐观锁字段',
  `tenant_id` bigint DEFAULT NULL COMMENT '租户id',
  `create_at` datetime NOT NULL COMMENT '创建时间的时间戳',
  `create_by` bigint DEFAULT NULL COMMENT '创建人',
  `update_at` datetime DEFAULT NULL COMMENT '修改时间的时间戳',
  `update_by` bigint DEFAULT NULL COMMENT '修改人',
  PRIMARY KEY (`id`) USING BTREE,
  UNIQUE KEY `uni_company_id__data_md5` (`company_id`,`data_md5`) USING BTREE,
  KEY `company_id` (`company_id`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='企业行政许可表';
