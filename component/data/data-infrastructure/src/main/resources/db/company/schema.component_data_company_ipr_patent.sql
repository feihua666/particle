-- 建表语句sql
DROP TABLE IF EXISTS component_data_company_ipr_patent;
CREATE TABLE `component_data_company_ipr_patent` (
  `id` bigint NOT NULL COMMENT '主键ID',
  `title` varchar(200) DEFAULT NULL COMMENT '原始标题，如：一种环境检测仪用支撑设备',
  `title_cn` varchar(200) DEFAULT NULL COMMENT '中文标题',
  `title_en` varchar(200) DEFAULT NULL COMMENT '英文标题',
  `apply_no` varchar(50) NOT NULL COMMENT '原始申请号，如：CN95106853.3',
  `standard_apply_no` varchar(50) NOT NULL COMMENT '标准申请号，如：CN101995000006852',
  `apply_date` date DEFAULT NULL COMMENT '申请日期',
  `public_no` varchar(50) DEFAULT NULL COMMENT '原始公布号',
  `standard_public_no` varchar(50) DEFAULT NULL COMMENT '标准公布号',
  `public_date` date DEFAULT NULL COMMENT '公布日期',
  `authorize_public_no` varchar(50) DEFAULT NULL COMMENT '授权公告号',
  `authorize_public_date` date DEFAULT NULL COMMENT '授权公告日期',
  `priority_no` varchar(50) DEFAULT NULL COMMENT '优先权号',
  `priority_date` date DEFAULT NULL COMMENT '优先权日期',
  `invalid_date` date DEFAULT NULL COMMENT '失效日期',
  `international_apply_no` varchar(50) DEFAULT NULL COMMENT '国际申请号',
  `international_apply_date` date DEFAULT NULL COMMENT '国际申请日期',
  `international_public_no` varchar(50) DEFAULT NULL COMMENT '国际公布号',
  `international_public_date` date DEFAULT NULL COMMENT '国际公布日期',
  `entry_country_date` date DEFAULT NULL COMMENT '进入国家日期',
  `split_apply_no` varchar(50) DEFAULT NULL COMMENT '分案申请号',
  `split_apply_date` date DEFAULT NULL COMMENT '分案申请日期',
  `reward_level` varchar(500) DEFAULT NULL COMMENT '原始奖励等级',
  `reward_name` varchar(500) DEFAULT NULL COMMENT '原始奖励名称',
  `reward_session` varchar(500) DEFAULT NULL COMMENT '原始奖励界次',
  `ipc_category_nos` varchar(500) DEFAULT NULL COMMENT 'IPC分类号，国际专利分类号，多个以英文逗号分隔',
  `ipc_main_category_no` varchar(50) DEFAULT NULL COMMENT '主IPC分类号',
  `cpc_category_nos` varchar(500) DEFAULT NULL COMMENT 'CPC分类号，联合专利分类，多个以英文逗号分隔',
  `cpc_main_category_no` varchar(50) DEFAULT NULL COMMENT '主CPC分类号',
  `uc_no` varchar(500) DEFAULT NULL COMMENT 'UC分类号，美国专利商标局（USPTO）的专利分类号，多个以英文逗号分隔',
  `fi_no` varchar(500) DEFAULT NULL COMMENT 'FI分类号，芬兰的专利分类号，多个以英文逗号分隔',
  `fterm_no` varchar(500) DEFAULT NULL COMMENT 'fterm分类号，File Term Search，专利检索的工具或方法分类号，多个以英文逗号分隔',
  `patent_type_dict_id` bigint DEFAULT NULL COMMENT '专利类型，字典id，指专利法律类型，如：发明、实用新型、外观设计',
  `receiving_office_name` varchar(100) DEFAULT NULL COMMENT '受理局名称，如：中国',
  `receiving_country_name` varchar(100) DEFAULT NULL COMMENT '受理国名称',
  `origin_country_name` varchar(100) DEFAULT NULL COMMENT '来源国名称',
  `literature_type` varchar(50) DEFAULT NULL COMMENT '文献类型，如：B',
  `appearance_scheme_no` varchar(50) DEFAULT NULL COMMENT '外观设计方案号',
  `appearance_scheme_no_supplement` varchar(200) DEFAULT NULL COMMENT '外观设计方案号补充',
  `national_economic_classification` varchar(200) DEFAULT NULL COMMENT '国民经济行业分类',
  `is_pct` tinyint(1) DEFAULT NULL COMMENT '是否为pct专利，1-是，0-否',
  `patent_image_url` varchar(300) DEFAULT NULL COMMENT '专利图片地址',
  `is_current_valid` tinyint(1) DEFAULT NULL COMMENT '是否当前有效，1-有效，0=无效，一般认为当前法律状态为申请恢复、授权、部分无效、权利恢复、有效期续展这几个状态时有效或当前权利状态为有权',
  `current_right_status_dict_id` bigint DEFAULT NULL COMMENT '当前权利状态，字典id',
  `instruction_manual_page_size` int DEFAULT NULL COMMENT '说明书页数',
  `locarno_category_nos` varchar(500) DEFAULT NULL COMMENT '洛迦诺分类号，多个以英文逗号分隔',
  `patent_strength` decimal(11,2) DEFAULT NULL COMMENT '专利强度,如：38.69',
  `latest_handle_at` datetime DEFAULT NULL COMMENT '最后处理时间，不代表数据有变动，用来表示数据处理过，但可能无需处理，不影响版本号变动',
  `version` int NOT NULL COMMENT '乐观锁字段',
  `tenant_id` bigint DEFAULT NULL COMMENT '租户id',
  `create_at` datetime NOT NULL COMMENT '创建时间的时间戳',
  `create_by` bigint DEFAULT NULL COMMENT '创建人',
  `update_at` datetime DEFAULT NULL COMMENT '修改时间的时间戳',
  `update_by` bigint DEFAULT NULL COMMENT '修改人',
  PRIMARY KEY (`id`) USING BTREE,
  UNIQUE KEY `apply_no` (`apply_no`) USING BTREE,
  UNIQUE KEY `public_no` (`public_no`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='企业知识产权专利表';
