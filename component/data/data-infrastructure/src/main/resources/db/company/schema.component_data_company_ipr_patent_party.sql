CREATE TABLE `component_data_company_ipr_patent_party` (
  `id` bigint NOT NULL COMMENT '主键ID',
  `company_ipr_patent_id` bigint NOT NULL COMMENT '企业知识产权专利表id',
  `party_name` varchar(100) DEFAULT NULL COMMENT '当事人名称原始名称，is_party_natural_person 等于0时为人名，等于1时为公司名',
  `party_name_en` varchar(100) DEFAULT NULL COMMENT '当事人名称英文名称，is_party_natural_person 等于0时为人名，等于1时为公司名',
  `party_name_cn` varchar(100) DEFAULT NULL COMMENT '当事人名称中文名称，is_party_natural_person 等于0时为人名，等于1时为公司名',
  `is_party_natural_person` tinyint(1) DEFAULT NULL COMMENT '是否当事人为自然人，1=自然人，0=非自然人',
  `party_company_id` bigint DEFAULT NULL COMMENT '当事人公司id，is_party_natural_person = 0 时有值',
  `party_company_person_id` bigint DEFAULT NULL COMMENT '当事人个人id，is_party_natural_person = 1 时有值',
  `is_applicant` tinyint(1) DEFAULT NULL COMMENT '是否申请人，1=申请人，0=非申请人',
  `is_invent` tinyint(1) DEFAULT NULL COMMENT '是否发明人，1=发明人，0=非发明人',
  `is_agent` tinyint(1) DEFAULT NULL COMMENT '是否代理人，1=代理人，0=非代理人',
  `is_agency` tinyint(1) DEFAULT NULL COMMENT '是否代理机构，1=代理机构，0=非代理机构',
  `is_examiner` tinyint(1) DEFAULT NULL COMMENT '是否审查员，1=审查员，0=非审查员',
  `is_right` tinyint(1) DEFAULT NULL COMMENT '是否权利人，1=权利人，0=非权利人',
  `is_current_right` tinyint(1) DEFAULT NULL COMMENT '是否当前权利人，1=当前权利人，0=非当前权利人',
  `address` varchar(200) DEFAULT NULL COMMENT '地址，主要是申请人地址、专利权人地址、当前权利人地址',
  `area_code` varchar(200) DEFAULT NULL COMMENT '区域编码，主要是申请人区域代码、当前权利人区域代码',
  `type_name` varchar(200) DEFAULT NULL COMMENT '类型，主要是当前权利人类型、专利权人类型、申请人类型',
  `code` varchar(200) DEFAULT NULL COMMENT '代码，主要是代理机构代码',
  `data_md5` varchar(32) NOT NULL COMMENT '数据md5,party_name + is_applicant + is_invent + is_agent + is_agency + is_examiner + is_right + is_current_right',
  `latest_handle_at` datetime DEFAULT NULL COMMENT '最后处理时间，不代表数据有变动，用来表示数据处理过，但可能无需处理，不影响版本号变动',
  `version` int NOT NULL COMMENT '乐观锁字段',
  `tenant_id` bigint DEFAULT NULL COMMENT '租户id',
  `create_at` datetime NOT NULL COMMENT '创建时间的时间戳',
  `create_by` bigint DEFAULT NULL COMMENT '创建人',
  `update_at` datetime DEFAULT NULL COMMENT '修改时间的时间戳',
  `update_by` bigint DEFAULT NULL COMMENT '修改人',
  PRIMARY KEY (`id`) USING BTREE,
  UNIQUE KEY `uni_company_ipr_patent_id__data_md5` (`company_ipr_patent_id`,`data_md5`) USING BTREE,
  KEY `company_ipr_patent_id` (`company_ipr_patent_id`) USING BTREE,
  KEY `party_company_id` (`party_company_id`) USING BTREE,
  KEY `party_company_person_id` (`party_company_person_id`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='企业知识产权专利当事人表';
