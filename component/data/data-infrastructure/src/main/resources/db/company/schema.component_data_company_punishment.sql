DROP TABLE IF EXISTS component_data_company_punishment;
CREATE TABLE `component_data_company_punishment` (
  `id` bigint NOT NULL COMMENT '主键ID',
  `company_id` bigint NOT NULL COMMENT '企业表ID',
  `company_name` varchar(100) DEFAULT NULL COMMENT '企业名称，（信用中国、工商公示）',
  `legal_person_name` varchar(100) DEFAULT NULL COMMENT '法人名称，is_legal_person_natural_person 等于0时为人名，等于1时为公司名，（信用中国、工商公示）',
  `is_legal_person_natural_person` tinyint(1) DEFAULT NULL COMMENT '是否法人为自然人，1=自然人，0=非自然人',
  `legal_person_company_id` bigint DEFAULT NULL COMMENT '法人公司id，is_legal_person_natural_person = 0 时有值',
  `legal_person_company_person_id` bigint DEFAULT NULL COMMENT '法人个人id，is_legal_person_natural_person = 1 时有值',
  `punish_no` varchar(100) DEFAULT NULL COMMENT '行政处罚决定书文号，（信用中国、工商公示）',
  `illegal_type` varchar(100) DEFAULT NULL COMMENT '违法行为类型 ，（信用中国、工商公示）',
  `punish_basis` text COMMENT '处罚依据，（信用中国、工商公示）',
  `illegal_fact` text COMMENT '违法事实，（信用中国、工商公示）',
  `punish_type` varchar(100) DEFAULT NULL COMMENT '处罚类别，（信用中国、工商公示）',
  `punish_content` text NOT NULL COMMENT '处罚内容，（信用中国、工商公示）',
  `fine_amount` decimal(16,5) DEFAULT NULL COMMENT '罚款金额（万元），（信用中国、工商公示）',
  `fine_amount_currency_dict_id` bigint DEFAULT NULL COMMENT '罚款金额币种，字典id，如：人民币',
  `confiscate_amount` decimal(16,5) DEFAULT NULL COMMENT '没收金额（万元），（信用中国、工商公示）',
  `confiscate_amount_currency_dict_id` bigint DEFAULT NULL COMMENT '没收金额币种，字典id，如：人民币',
  `institute_company_id` bigint DEFAULT NULL COMMENT '作出行政处罚决定机关公司id',
  `institute_name` varchar(150) DEFAULT NULL COMMENT '作出行政处罚决定机关名称，冗余公司名称，（信用中国、工商公示）',
  `suspend_or_revoke_license_name_code` varchar(150) DEFAULT NULL COMMENT '暂扣或吊销证照名称及编号，（信用中国、工商公示）',
  `punish_decision_start_date` date DEFAULT NULL COMMENT '作出行政处罚决定开始日期，作出行政处罚决定日期，（信用中国、工商公示）',
  `punish_decision_end_date` date DEFAULT NULL COMMENT '作出行政处罚决定结束日期，处罚有效期，（工商公示）',
  `publish_start_date` date DEFAULT NULL COMMENT '发布开始日期，公示日期，（工商公示）',
  `publish_end_date` date DEFAULT NULL COMMENT '发布结束日期，公示截止期，（工商公示）',
  `data_from` varchar(100) DEFAULT NULL COMMENT '数据来源，如：盂县消防救援大队，（信用中国）',
  `data_from_company_id` bigint DEFAULT NULL COMMENT '数据来源公司id',
  `data_from_name` varchar(150) DEFAULT NULL COMMENT '数据来源名称，冗余公司名称，如：盂县消防救援大队（信用中国、工商公示）',
  `remark` varchar(255) DEFAULT NULL COMMENT '备注，（工商公示）',
  `is_data_flag_gs` tinyint(1) DEFAULT NULL COMMENT '是否数据标识为工商公示，1=工商公示，0=非工商公示',
  `is_data_flag_xyzg` tinyint(1) DEFAULT NULL COMMENT '是否数据标识为信用中国，1=信用中国，0=非信用中国',
  `data_md5` varchar(32) DEFAULT NULL COMMENT '数据md5,company_name + punish_no + punish_content',
  `latest_handle_at` datetime DEFAULT NULL COMMENT '最后处理时间，不代表数据有变动，用来表示数据处理过，但可能无需处理，不影响版本号变动',
  `version` int NOT NULL COMMENT '乐观锁字段',
  `tenant_id` bigint DEFAULT NULL COMMENT '租户id',
  `create_at` datetime NOT NULL COMMENT '创建时间的时间戳',
  `create_by` bigint DEFAULT NULL COMMENT '创建人',
  `update_at` datetime DEFAULT NULL COMMENT '修改时间的时间戳',
  `update_by` bigint DEFAULT NULL COMMENT '修改人',
  PRIMARY KEY (`id`) USING BTREE,
  KEY `company_id` (`company_id`) USING BTREE,
  KEY `punish_no` (`punish_no`) USING BTREE,
  KEY `is_data_flag_gs` (`is_data_flag_gs`) USING BTREE,
  KEY `is_data_flag_xyzg` (`is_data_flag_xyzg`) USING BTREE,
  UNIQUE KEY `uni_company_id__data_md5` (`company_id`,`data_md5`) USING BTREE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin COMMENT='企业行政处罚表';
