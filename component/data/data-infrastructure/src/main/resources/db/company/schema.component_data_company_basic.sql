  UNIQUE KEY `company_id` (`company_id`) USING BTREE,
  KEY `industry_main_dict_id` (`industry_main_dict_id`) USING BTREE,
  KEY `industry_big_dict_id` (`industry_big_dict_id`) USING BTREE,
  KEY `industry_middle_dict_id` (`industry_middle_dict_id`) USING BTREE,
  KEY `industry_small_dict_id` (`industry_small_dict_id`) USING BTREE,
  KEY `province_area_id` (`province_area_id`) USING BTREE,
  KEY `city_area_id` (`city_area_id`) USING BTREE,
  KEY `county_area_id` (`county_area_id`) USING BTREE,
  KEY `status_dict_id` (`status_dict_id`) USING BTREE,
  KEY `is_listed` (`is_listed`) USING BTREE,
  KEY `establish_date` (`establish_date`) USING BTREE
