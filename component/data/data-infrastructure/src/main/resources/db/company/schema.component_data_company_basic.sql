  `latitude` varchar(50) DEFAULT NULL COMMENT '纬度',
  `industry_main_dict_id` bigint DEFAULT NULL COMMENT '所属行业，字典id，根据国家统计局将国民经济行业划分为门类、大类、中类和小类四级，本字段存储门类',
  `industry_big_dict_id` bigint DEFAULT NULL COMMENT '所属行业，字典id，根据国家统计局将国民经济行业划分为门类、大类、中类和小类四级，本字段存储大类类',
  `industry_middle_dict_id` bigint DEFAULT NULL COMMENT '所属行业，字典id，根据国家统计局将国民经济行业划分为门类、大类、中类和小类四级，本字段存储中类',
  `industry_small_dict_id` bigint DEFAULT NULL COMMENT '所属行业，字典id，根据国家统计局将国民经济行业划分为门类、大类、中类和小类四级，本字段存储小类',
  `province_area_id` bigint DEFAULT NULL COMMENT '所在省份，区域id',
  `city_area_id` bigint DEFAULT NULL COMMENT '所在城市，区域id',
  `county_area_id` bigint DEFAULT NULL COMMENT '所在区县，区域id',
  `is_listed` tinyint(1) DEFAULT NULL COMMENT '是否上市',
