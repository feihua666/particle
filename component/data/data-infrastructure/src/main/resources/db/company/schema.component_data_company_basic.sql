  `establish_date` date DEFAULT NULL COMMENT '成立日期',
  `business_from_date` date DEFAULT NULL COMMENT '营业期限开始日期',
  `business_to_date` date DEFAULT NULL COMMENT '营业期限终止日期',
  `approve_date` date DEFAULT NULL COMMENT '核准日期',
  `cancel_date` date DEFAULT NULL COMMENT '注销日期',
  `cancel_reason` varchar(200) DEFAULT NULL COMMENT '注销原因',
  `revoke_date` date DEFAULT NULL COMMENT '吊销日期',
  `revoke_reason` varchar(200) DEFAULT NULL COMMENT '吊销原因',
  `business_scope` text COMMENT '经营范围',
  `reg_institute_company_id` bigint DEFAULT NULL COMMENT '注册机关公司id',
  `reg_institute_name` varchar(150) DEFAULT NULL COMMENT '注册机关名称，冗余公司名称',
  `reg_capital` decimal(16,5) DEFAULT NULL COMMENT '注册资本（万元）',
  `reg_capital_currency_dict_id` bigint DEFAULT NULL COMMENT '注册资金币种，字典id，如：人民币',
  `sub_capital` decimal(16,5) DEFAULT NULL COMMENT '认缴出资金额（万元）',
  `sub_capital_currency_dict_id` bigint DEFAULT NULL COMMENT '认缴出资金额币种，字典id，如：人民币',
  `sub_capital_type_dict_id` bigint DEFAULT NULL COMMENT '认缴出资方式',
  `sub_capital_date` date DEFAULT NULL COMMENT '认缴出资日期',
  `actual_capital` decimal(16,5) DEFAULT NULL COMMENT '实缴出资金额（万元）',
  `actual_capital_currency_dict_id` bigint DEFAULT NULL COMMENT '实缴出资金额币种，字典id，如：人民币',
  `capital_type_dict_id` bigint DEFAULT NULL COMMENT '实缴出资方式',
  `actual_capital_date` date DEFAULT NULL COMMENT '实缴出资日期',
  `mobile` varchar(20) DEFAULT NULL COMMENT '手机号码',
  `telephone` varchar(20) DEFAULT NULL COMMENT '电话号码',
  `email` varchar(200) DEFAULT NULL COMMENT '邮箱',
  `employee_num` int DEFAULT NULL COMMENT '从业人数',
