  `year` int NOT NULL COMMENT '年报年度',
  `serial_number` int DEFAULT NULL COMMENT '序号',
  `debtor_name` varchar(100) DEFAULT NULL COMMENT '债务人名称',
  `is_debtor_natural_person` tinyint(1) DEFAULT NULL COMMENT '是否债务人为自然人，1=自然人，0=非自然人',
