  `guarantee_term_dict_id` bigint DEFAULT NULL COMMENT '保证的期间，字典id，如：期限、未约定',
  `guarantee_type_dict_id` bigint DEFAULT NULL COMMENT '保证的方式，字典id，如：一般保证、连带保证、未约定',
