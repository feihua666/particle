  `creditor_name` varchar(100) DEFAULT NULL COMMENT '债权人名称',
  `is_creditor_natural_person` tinyint(1) DEFAULT NULL COMMENT '是否债权人为自然人，1=自然人，0=非自然人',
  `creditor_company_id` bigint DEFAULT NULL COMMENT '债权人公司id，is_creditor_natural_person = 0 时有值',
  `creditor_company_person_id` bigint DEFAULT NULL COMMENT '债权人个人id，is_creditor_natural_person = 1 时有值',
  `credito_type_dict_id` bigint DEFAULT NULL COMMENT '主债权种类，字典id，如：合同、其它',
