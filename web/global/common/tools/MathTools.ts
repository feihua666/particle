/**
 * 取两个数的最小值
 * @param x
 * @param y
 * @return {*}
 */
export function min(x: number,y: number):number {
    return Math.min(x,y)
}
/**
 * 取两个数的最大值
 * @param x
 * @param y
 * @return {*}
 */
export function max(x: number,y: number): number {
    return Math.max(x,y)
}

/**
 * 向下取整
 * @param x
 * @return {number}
 */
export function floor(x: number): number {
    return Math.floor(x)
}